package aulinha;

public class FluxoCondicional {
    public static void main(String[] args) {
        int x = 4;
        if(x > 4) {
            System.out.println("X é maior do que 4");
        }
        else if(x == 4) {
            System.out.println("X é igual a 4");
        }
        else {
            System.out.println("X não é maior que 4");
        }
    }
}
